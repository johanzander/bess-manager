def verify_bess_total() -> float:
    """Sanity check: sum of real per-period profit should match the
    bundle's own recorded total (2.0532 EUR) before trusting anything
    downstream."""
    total = sum(
        BESS_EXPORT_REVENUE[i] - BESS_IMPORT_COST[i] - BESS_BATTERY_CYCLE_COST[i]
        for i in range(len(BESS_EXPORT_REVENUE))
    )
    print(f"BESS total profit (full 129-period horizon): {total:.4f} EUR")
    return total


def isolate_morning_mistiming() -> None:
    print()
    print(
        "=== Morning mistiming: battery-attributable revenue vs. best available price ==="
    )
    total_kwh = 0.0
    actual_revenue = 0.0
    for i in MISTIMED_INDICES:
        b2g = BESS_BATTERY_TO_GRID[i]
        sell = SELL_PRICE[i]
        rev = b2g * sell
        total_kwh += b2g
        actual_revenue += rev
        print(
            f"  idx={i:3d}  battery_to_grid={b2g:.4f} kWh  sell={sell:.4f}  revenue={rev:.4f}"
        )

    print(
        f"\n  Total battery-attributable kWh sold in mistimed window: {total_kwh:.4f}"
    )
    print(f"  Actual revenue at these (low) prices: {actual_revenue:.4f} EUR")

    best_sell = SELL_PRICE[BEST_EVENING_INDICES[0]]
    headroom = sum(
        MAX_DISCHARGE_OUTPUT_KWH - abs(BESS_BATTERY_ACTION[i])
        for i in BEST_EVENING_INDICES
    )
    print(f"\n  Best evening sell price (21:00-21:45, 13 Jul): {best_sell:.4f} EUR/kWh")
    print(
        f"  Unused discharge headroom available there: {headroom:.4f} kWh (>> {total_kwh:.4f} needed)"
    )

    counterfactual_revenue = total_kwh * best_sell
    print(
        f"\n  Counterfactual revenue if sold there instead: {counterfactual_revenue:.4f} EUR"
    )
    print(
        f"  Value left on the table: {counterfactual_revenue - actual_revenue:.4f} EUR"
    )


